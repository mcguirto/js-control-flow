let age = 11

if(age >= 18){
    console.log('Adult')
} else if (age < 18 && age > 13){
    console.log('Teen')
} else if (age <= 12 && age > 10) {
    console.log('preteen')
}else if (age <= 9 && age > 6) {
    console.log('child')
}else if (age <= 5 && age > 2){
    console.log('toddler')
}else if (age = 1){
    console.log('infant')
}